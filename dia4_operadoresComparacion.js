//Operadores de comparación - devuelven un valor booleano para indicar si la operación es verdadera o falsa.

console.log(7 > 10); //flase
console.log(1 < 9); //true
console.log(15 < 10); //flase
console.log(29 > 20); //true
console.log('***** Fin operadores de comparación *****'); //true

//Igualdad vs igualdad estricta 
console.log (3 == 3); //true
console.log (3 === 3); //true
console.log (3 == "3"); //true
console.log (3 === "3"); //false
console.log (7 == "7"); //true
console.log(7 === "7"); //false
console.log('***** Fin operadores vs igualdad estricta *****');

//Asignación (=)
var miNUmero = 70;
console.log(miNUmero); //70

//Comparación (==) - compara únicamente el valor
console.log(9 == 8); //flase
console.log(5 == '5'); //true

//Igualdad estricta (===) - compara tipo de dato y el valor 
console.log(10 === '10'); //false
console.log(10 === 10); //true

