//suma(+)
console.log('suma: ', 15 + 17); //suma: 32

//resta(-)
console.log('resta: ', 79 - 36); //resta: 43

//multiplicación(*)
console.log('multiplicación: ', 10 * 9); //multiplicación: 90

//división(/)
console.log('división: ', 125 / 5); //división: 25

//resto(%) - devuelve el resto de la división
console.log('resto: ', 49 % 5); //resto: 4

//presedencia de operadores - orden en el cual los operadores son evaluados
//en el siguiente ejemplo se evalua primeramente la multiplicación luego la suma y por último la resta
console.log('resultado: ', 12 + 7 * 5 - 10); //resultado: 37

//uso del operador '+' para concatenar string
var nombre = 'Melvin ';
var apellido = 'Vargas';

console.log(nombre + apellido);//Melvin Vargas
